    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int numVertices = 7;

        DfsAlgorithm graphPanel = new DfsAlgorithm(numVertices);
        graphPanel.setPreferredSize(new Dimension(500, 500));

        // Adding edges between vertices
        graphPanel.addEdge(0, 1);
        graphPanel.addEdge(0, 2);
        graphPanel.addEdge(1, 3);
        graphPanel.addEdge(1, 4);
        graphPanel.addEdge(2, 5);
        graphPanel.addEdge(2, 6);

        JFrame frame = new JFrame();
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        frame.setTitle("Graph DFS Visualization");
        frame.getContentPane().add(graphPanel);
        frame.pack();
        frame.setVisible(true);

        // Perform DFS traversal starting from vertex 0
        graphPanel.DFS(0);
          
    }//GEN-LAST:event_jButton4ActionPerformed

