    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String input = JOptionPane.showInputDialog("Enter numbers separated by commas:");
        String[] numbers = input.split("\\s*,\\s*");
        int[] arr = new int[numbers.length];

        for (int i = 0; i < numbers.length; i++) {
            arr[i] = Integer.parseInt(numbers[i]);
        }

        MergeSortVisualization visualization = new MergeSortVisualization(arr);

        JFrame frame = new JFrame("Merge Sort Visualization");
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        frame.setResizable(false);
        frame.getContentPane().add(visualization);
        frame.pack();
        frame.setLocationRelativeTo(null);
        frame.setVisible(true);

        visualization.mergeSort();

        // Show the sorted array
        String sortedArrayMessage = "Sorted Array: " + Arrays.toString(visualization.sortedArr);
        JOptionPane.showMessageDialog(frame, sortedArrayMessage, "Sorted Array", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton5ActionPerformed

